# Split the input string into lines
lines = input_string.replace('#*# 	','')
lines = lines.strip().split("\n")

# Split each line into a list of numbers, handling the ValueError
points = []
for line in lines:
    try:
        points.append(list(map(float, line.split(", "))))
    except ValueError:
        # Split the line at the newline character and convert each part to float
        parts = line.split("\n")
        for part in parts:
            points.append(list(map(float, part.split(", "))))

# Print the formatted string for np.array
print("np.array([")
for point in points:
    print("    " + str(point) + ",")
